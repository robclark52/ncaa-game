// Lookup function with fuzzy matching
function lookupRPI(teamName) {
    if (!teamName) return null;
    const name = teamName.trim();
    // Direct lookup
    if (RPI_DATA[name] !== undefined) return RPI_DATA[name];
    // Try common variations
    const variations = [
        name,
        name.replace(/\.$/, ''),
        name.replace(/St\.?$/, 'State'),
        name.replace(/State$/, 'St'),
        name.replace(/St\.?$/, 'St.'),
    ];
    for (const v of variations) {
        if (RPI_DATA[v] !== undefined) return RPI_DATA[v];
    }
    // Partial match
    const lower = name.toLowerCase();
    for (const [key, val] of Object.entries(RPI_DATA)) {
        if (key.toLowerCase() === lower) return val;
        if (key.toLowerCase().includes(lower) || lower.includes(key.toLowerCase())) return val;
    }
    return null;
}
